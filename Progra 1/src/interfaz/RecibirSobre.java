    private void aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarActionPerformed
        String cont = contenido.getText();
        String tipo = tipoSobre.getText();
        String ced = cedulaCliente.getText();
        String pes = peso.getText();
        double Dpes = Double.parseDouble(pes);
        String rem = remitente.getText();
        String num = numReferencia.getText();
        int numR = Integer.parseInt(num);
        String des = descripcion.getText();
        Sobre nuevo = new Sobre(numR,false,des,rem,tipo,cont,Dpes);
        Counter count = Funcionalidades.getCounter();
        count.registrarSobre(ced, nuevo);
        Funcionalidades.setCounter(count);
        JOptionPane.showMessageDialog(null, "se registro el sobre");
        if(count.detectarCambioDeNivel(ced)){
            JOptionPane.showMessageDialog(null, "Subio de rango!");
        }
        close();
        FuncionalidadesI fun = new FuncionalidadesI();
        fun.setVisible(true);
    }//GEN-LAST:event_aceptarActionPerformed

